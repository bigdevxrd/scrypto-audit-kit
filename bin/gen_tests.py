#!/usr/bin/env python3
"""Generate scrypto-test property-test scaffolds from a blueprint's surface.

Deterministic, no API. Reads the `enable_method_auth!` surface (roles + per-method gating)
and the struct's Vault fields, then emits compilable `#[test] #[ignore]` scaffolds with
`todo!()` bodies:

  - auth negative-path tests for each role-restricted method (the most-missed coverage),
  - happy-path tests for each public method,
  - a value-conservation invariant test when the blueprint custodies vaults.

The scaffolds compile and are `#[ignore]`d, so they never break `cargo test`; the developer
(or an agent) removes `#[ignore]` and fills in each `todo!()`. The kit stays read-only — this
prints/returns the scaffolds, it never writes into the audited package.

Stdlib only; reuses the comment/string stripper from static_analysis. Importable + CLI.
"""
import argparse
import json
import os
import re
import sys

import static_analysis

_GATING_RE = re.compile(r"(\w+)\s*=>\s*(PUBLIC|restrict_to:\s*\[\s*([\w, ]+?)\s*\])")
_IMPL_RE = re.compile(r"\bimpl\s+(\w+)")
_VAULT_RE = re.compile(r"(\w+)\s*:\s*(?:Vault|KeyValueStore<[^>]*\bVault\b[^>]*>)")


def _stripped_sources(pkg_dir):
    src_dir = os.path.join(pkg_dir, "src")
    base = src_dir if os.path.isdir(src_dir) else pkg_dir
    chunks = []
    for root, _dirs, files in os.walk(base):
        for name in sorted(files):
            if name.endswith(".rs"):
                with open(os.path.join(root, name), encoding="utf-8", errors="replace") as fh:
                    chunks.append(static_analysis.strip_comments_and_strings(fh.read()))
    return "\n".join(chunks)


def extract_surface(pkg_dir):
    """Return {blueprint, methods:[{name,gating,roles}], roles:[...], vault_fields:[...]}."""
    src = _stripped_sources(pkg_dir)

    methods, roles = [], set()
    for m in _GATING_RE.finditer(src):
        name, gating = m.group(1), m.group(2)
        if gating.startswith("PUBLIC"):
            methods.append({"name": name, "gating": "public", "roles": []})
        else:
            mr = [r.strip() for r in m.group(3).split(",") if r.strip()]
            roles.update(mr)
            methods.append({"name": name, "gating": "restricted", "roles": mr})

    impls = _IMPL_RE.findall(src)
    blueprint = impls[0] if impls else "Blueprint"
    vault_fields = sorted(set(_VAULT_RE.findall(src)))
    return {"blueprint": blueprint, "methods": methods, "roles": sorted(roles), "vault_fields": vault_fields}


def propose_specs(surface):
    """Turn a surface into an ordered list of property-test specs."""
    specs = []
    for meth in surface["methods"]:
        if meth["gating"] == "restricted":
            badge = " / ".join(meth["roles"]) or "required"
            specs.append({
                "name": f"{meth['name']}_rejects_unauthorized_caller",
                "kind": "auth-negative",
                "target": meth["name"],
                "invariant": f"`{meth['name']}` must fail when called without the `{badge}` badge",
                "hint": f"instantiate, then call {meth['name']} WITHOUT the {badge} badge and assert "
                        f"the call fails (an auth error)",
            })
    for meth in surface["methods"]:
        if meth["gating"] == "public":
            specs.append({
                "name": f"{meth['name']}_happy_path",
                "kind": "happy-path",
                "target": meth["name"],
                "invariant": f"`{meth['name']}` succeeds on a valid call and makes the expected state change",
                "hint": f"instantiate, call {meth['name']} with valid inputs, assert the expected result/state",
            })
    if surface["vault_fields"]:
        fields = ", ".join(surface["vault_fields"])
        specs.append({
            "name": "value_conservation_invariant",
            "kind": "invariant",
            "target": None,
            "invariant": "custodied value is conserved across operations (no value created or lost)",
            "hint": f"after each public call, assert the sum of vault balances ({fields}) matches the "
                    f"tracked totals — value is neither minted nor lost",
        })
    return specs


def render_rust(specs, surface):
    """Render specs as a compilable, #[ignore]d scrypto-test scaffold file."""
    out = [
        "//! Property-test scaffolds generated by scrypto-audit-kit.",
        f"//! Blueprint: {surface['blueprint']}. These compile and are #[ignore]d — remove the",
        "//! #[ignore] and fill in each todo!() to turn a coverage gap into an executable check.",
        "#![allow(unused)]",
        "use scrypto_test::prelude::*;",
        "",
    ]
    if not specs:
        out.append("// No role-restricted methods, public methods, or vaults detected.")
        out.append("")
        return "\n".join(out)
    for spec in specs:
        out.append(f"/// {spec['invariant']}.")
        out.append("#[test]")
        out.append('#[ignore = "scaffold from scrypto-audit-kit — implement, then un-ignore"]')
        out.append(f"fn {spec['name']}() {{")
        out.append(f'    todo!("{spec["hint"]}");')
        out.append("}")
        out.append("")
    return "\n".join(out)


def propose_tests(pkg_dir):
    """Top-level: returns {blueprint, count, specs, rust} for a package."""
    surface = extract_surface(pkg_dir)
    specs = propose_specs(surface)
    return {
        "blueprint": surface["blueprint"],
        "count": len(specs),
        "specs": specs,
        "rust": render_rust(specs, surface),
    }


def main():
    ap = argparse.ArgumentParser(description="Generate scrypto-test property-test scaffolds.")
    ap.add_argument("package", help="path to the Scrypto package (or a src dir)")
    ap.add_argument("--json", action="store_true", help="print the specs as JSON instead of Rust")
    ap.add_argument("--out", help="write the Rust scaffold to this file")
    args = ap.parse_args()

    result = propose_tests(args.package)
    if args.json:
        json.dump({k: result[k] for k in ("blueprint", "count", "specs")}, sys.stdout, indent=2)
        sys.stdout.write("\n")
    elif args.out:
        with open(args.out, "w", encoding="utf-8") as fh:
            fh.write(result["rust"])
    else:
        sys.stdout.write(result["rust"])
    sys.stderr.write(f"[gen-tests] {result['count']} scaffold(s) for {result['blueprint']}\n")
    return 0


if __name__ == "__main__":
    sys.exit(main())
